CREATE TABLE Products
(
Maker CHAR(1),
Model NUMBER(4) PRIMARY KEY,
Type VARCHAR2(7)
)

CREATE TABLE PC
(
Model NUMBER(4) PRIMARY KEY,
Speed NUMBER(4),
Ram NUMBER(3),
HD NUMBER(2),
Price NUMBER(4) NOT NULL
)

CREATE TABLE LAPTOP
(
Model NUMBER(4) PRIMARY KEY,
Speed NUMBER(3),
Ram NUMBER(3),
HD NUMBER(3),
Screen NUMBER(3,1),
Price NUMBER(4) NOT NULL
)

CREATE TABLE PRINTER
(
Model NUMBER(4),
Color VARCHAR2(5),
Type VARCHAR2(7),
Price NUMBER(4)
)

